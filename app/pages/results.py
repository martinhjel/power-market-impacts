"""
Results Visualization Page

This page reads and visualizes data from the results/results.h5 file
generated by the EMPS simulation runs.
"""

from pathlib import Path

import h5py
import pandas as pd
import plotly.express as px
import plotly.graph_objects as go
import streamlit as st

st.set_page_config(layout="wide")
st.title("📈 Results Visualization")

# Select simulation folder
result_paths = sorted(
    [i for i in (Path.cwd() / "ltm_output").glob("*/*") if i.is_dir()],
    key=lambda p: p.name.lower(),
)


def _result_label(p: Path) -> str:
    """Return a short label for a result path."""
    parent = p.parent.name
    if "1H" in parent:
        res = "1H"
    elif "1D" in parent:
        res = "1D"
    else:
        res = parent
    return f"{p.name} ({res})"


selected_path = st.sidebar.selectbox(
    "Select simulation:",
    result_paths,
    format_func=_result_label,
    index=0,
)

st.sidebar.markdown(f"**Path:** `{selected_path}`")

results_file = selected_path / "results/results.h5"

# Check if results file exists
if not results_file.exists():
    st.error(f"Results file not found: {results_file}")
    st.info("Run the simulation first to generate results.h5")
    st.stop()

# Validate HDF5 file
try:
    with open(results_file, "rb") as f:
        signature = f.read(8)
        if not signature.startswith(b"\x89HDF"):
            st.error(f"Invalid HDF5 file: {results_file}")
            st.stop()
except Exception as e:
    st.error(f"Cannot read file: {e}")
    st.stop()


@st.cache_data
def load_results_structure(file_path: Path) -> dict:
    """Load the structure of the HDF5 file."""
    structure = {}
    with h5py.File(file_path, "r") as f:

        def explore_group(group, prefix=""):
            items = {}
            for key in group.keys():
                full_path = f"{prefix}/{key}" if prefix else key
                if isinstance(group[key], h5py.Group):
                    items[key] = explore_group(group[key], full_path)
                else:
                    items[key] = {
                        "path": full_path,
                        "shape": group[key].shape,
                        "dtype": str(group[key].dtype),
                    }
            return items

        structure = explore_group(f)
    return structure


@st.cache_data
def load_dataset(file_path: Path, dataset_path: str) -> pd.DataFrame:
    """Load a dataset from HDF5 file as DataFrame."""
    with h5py.File(file_path, "r") as f:
        # Check if this is a vals dataset with corresponding times
        if dataset_path.endswith("/vals"):
            times_path = dataset_path.replace("/vals", "/times")
            if times_path in f:
                # Load values and times
                data = f[dataset_path][:]
                times = f[times_path][:]

                # Convert Unix timestamps to datetime
                try:
                    index = pd.to_datetime(times)
                except:
                    # Fallback: try as-is
                    index = times

                df = pd.DataFrame(data, index=index)
            else:
                # No times found, just load values
                data = f[dataset_path][:]
                df = pd.DataFrame(data)
        else:
            # Regular dataset
            data = f[dataset_path][:]
            df = pd.DataFrame(data)

        # Try to get unit attribute
        try:
            unit = f[dataset_path].attrs.get("unit", None)
            if unit:
                df.attrs["unit"] = unit if isinstance(unit, str) else unit.decode("utf-8")
        except:
            pass

        return df


# Load structure
with st.spinner("Loading results structure..."):
    structure = load_results_structure(results_file)

# Display available categories
st.header("Available Result Categories")

categories = list(structure.keys())
st.write(f"Found {len(categories)} categories: {', '.join(categories)}")

# Category selection
selected_category = st.selectbox("Select category:", categories)

if selected_category:
    st.subheader(f"Category: {selected_category}")

    # Get all datasets in this category (filter for vals datasets)
    def get_datasets(struct, prefix=""):
        datasets = []
        for key, value in struct.items():
            current_path = f"{prefix}/{key}" if prefix else key
            if isinstance(value, dict) and "path" in value:
                # Only include vals datasets (skip times datasets)
                if not value["path"].endswith("/times"):
                    # Use the parent path without /vals for display
                    display_name = value["path"].replace(f"/{selected_category}/", "").replace("/vals", "")
                    datasets.append((display_name, value))
            elif isinstance(value, dict):
                datasets.extend(get_datasets(value, current_path))
        return datasets

    datasets = get_datasets(structure[selected_category], selected_category)

    if datasets:
        st.write(f"Found {len(datasets)} datasets")

        # Dataset selection
        dataset_names = [name for name, _ in datasets]
        selected_dataset = st.selectbox("Select dataset:", dataset_names)

        if selected_dataset:
            # Find the full path
            dataset_info = next(info for name, info in datasets if name == selected_dataset)
            dataset_path = dataset_info["path"]

            st.info(f"**Path:** `{dataset_path}`")
            st.write(f"**Shape:** {dataset_info['shape']}")
            st.write(f"**Type:** {dataset_info['dtype']}")

            # Load and display data
            with st.spinner(f"Loading {selected_dataset}..."):
                df = load_dataset(results_file, dataset_path)

            # Display unit if available
            if hasattr(df, "attrs") and "unit" in df.attrs:
                st.write(f"**Unit:** {df.attrs['unit']}")

            # Tabs for different views
            tab1, tab2, tab3, tab4 = st.tabs(["📊 Plot", "📋 Data Table", "📈 Statistics", "🔢 Raw Data"])

            with tab1:
                st.subheader("Visualization")

                # Plot type selection
                plot_type = st.radio(
                    "Plot type:",
                    ["Line Chart", "Heatmap", "Box Plot", "Histogram"],
                    horizontal=True,
                )

                if plot_type == "Line Chart":
                    if df.shape[1] > 20:
                        st.warning(f"Dataset has {df.shape[1]} columns. Showing first 20.")
                        df_plot = df.iloc[:, :20]
                    else:
                        df_plot = df

                    fig = go.Figure()
                    for col in df_plot.columns:
                        fig.add_trace(
                            go.Scatter(
                                x=df_plot.index,
                                y=df_plot[col],
                                mode="lines",
                                name=str(col),
                            )
                        )

                    fig.update_layout(
                        title=selected_dataset,
                        xaxis_title="Time" if isinstance(df.index, pd.DatetimeIndex) else "Index",
                        yaxis_title=df.attrs.get("unit", "Value") if hasattr(df, "attrs") else "Value",
                        hovermode="x unified",
                        height=600,
                    )
                    st.plotly_chart(fig, use_container_width=True)

                elif plot_type == "Heatmap":
                    fig = go.Figure(
                        data=go.Heatmap(
                            z=df.values,
                            x=df.columns,
                            y=df.index,
                            colorscale="Viridis",
                        )
                    )
                    fig.update_layout(
                        title=selected_dataset,
                        height=600,
                    )
                    st.plotly_chart(fig, use_container_width=True)

                elif plot_type == "Box Plot":
                    fig = go.Figure()
                    for col in df.columns[:20]:  # Limit to 20 columns
                        fig.add_trace(go.Box(y=df[col], name=str(col)))

                    fig.update_layout(
                        title=selected_dataset,
                        yaxis_title=df.attrs.get("unit", "Value") if hasattr(df, "attrs") else "Value",
                        height=600,
                    )
                    st.plotly_chart(fig, use_container_width=True)

                elif plot_type == "Histogram":
                    if df.shape[1] == 1:
                        fig = px.histogram(df, x=df.columns[0])
                    else:
                        # Show histogram for mean across columns
                        fig = px.histogram(x=df.mean(axis=1), labels={"x": "Mean Value"})

                    fig.update_layout(
                        title=selected_dataset,
                        height=600,
                    )
                    st.plotly_chart(fig, use_container_width=True)

            with tab2:
                st.subheader("Data Table")

                # Show options
                col1, col2 = st.columns(2)
                with col1:
                    show_rows = st.number_input("Rows to display:", 1, len(df), min(100, len(df)))
                with col2:
                    show_cols = st.number_input("Columns to display:", 1, df.shape[1], min(20, df.shape[1]))

                st.dataframe(df.iloc[:show_rows, :show_cols], use_container_width=True)

                # Download button
                csv = df.to_csv()
                st.download_button(
                    label="📥 Download as CSV",
                    data=csv,
                    file_name=f"{selected_dataset}.csv",
                    mime="text/csv",
                )

            with tab3:
                st.subheader("Statistics")

                col1, col2 = st.columns(2)

                with col1:
                    st.write("**Basic Statistics**")
                    st.dataframe(df.describe(), use_container_width=True)

                with col2:
                    st.write("**Additional Info**")
                    st.write(f"- **Rows:** {df.shape[0]}")
                    st.write(f"- **Columns:** {df.shape[1]}")
                    st.write(f"- **Total values:** {df.size}")
                    st.write(f"- **Missing values:** {df.isna().sum().sum()}")
                    st.write(f"- **Memory usage:** {df.memory_usage().sum() / 1024 / 1024:.2f} MB")

                # Correlation matrix if multiple columns
                if df.shape[1] > 1 and df.shape[1] <= 50:
                    st.write("**Correlation Matrix**")
                    corr = df.corr()
                    fig = go.Figure(
                        data=go.Heatmap(
                            z=corr.values,
                            x=corr.columns,
                            y=corr.index,
                            colorscale="RdBu",
                            zmid=0,
                        )
                    )
                    fig.update_layout(height=600)
                    st.plotly_chart(fig, use_container_width=True)

            with tab4:
                st.subheader("Raw Data")
                st.write("First 10 rows:")
                st.code(df.head(10).to_string())

    else:
        st.warning(f"No datasets found in category '{selected_category}'")

# Footer
st.divider()
st.caption(f"Results file: {results_file}")
